///[IconData] is basically used for FontAwesome Brand icons
class FABrandIconData extends IconData {
  const FABrandIconData(int code)
      : super(
          code,
          fontFamily: 'FontAwesomeBrands',
          fontPackage: 'icons_plus',
        );
}

